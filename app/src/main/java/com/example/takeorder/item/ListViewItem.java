package com.example.takeorder.item;

public class ListViewItem {

    private int type;
    private String drinkKorea;
    private String drinkEnglish;
    private String drink;
    private int count;

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public String getDrinkKorea() {
        return drinkKorea;
    }

    public void setDrinkKorea(String drinkKorea) {
        this.drinkKorea = drinkKorea;
    }

    public String getDrinkEnglish() {
        return drinkEnglish;
    }

    public void setDrinkEnglish(String drinkEnglish) {
        this.drinkEnglish = drinkEnglish;
    }

    public String getDrink() {
        return drink;
    }

    public void setDrink(String drink) {
        this.drink = drink;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
